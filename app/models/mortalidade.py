class MortalidadeObj():
    def __init__(self):
        self.idMortalidade = None
        self.numeroDeCasos = None
        self.tipoDeCancer = None
        self.raca = None
        self.FaixaEtaria = None
        self.Ano_idAno = None

    def get_idMortalidade(self):
        return self.idMortalidade

    def set_idMortalidade(self, idMortalidade) :
        self.idMortalidade = idMortalidade

    def get_numeroDeCasos(self):
        return self.numeroDeCasos

    def set_numeroDeCasos(self, numeroDeCasos) :
        self.numeroDeCasos = numeroDeCasos

    def get_tipoDeCancer(self):
        return self.tipoDeCancer

    def set_tipoDeCancer(self, tipoDeCancer) :
        self.tipoDeCancer = tipoDeCancer

    def get_raca(self):
        return self.raca

    def set_raca(self, raca) :
        self.raca = raca

    def get_FaixaEtaria(self):
        return self.FaixaEtaria

    def set_FaixaEtaria(self, FaixaEtaria) :
        self.FaixaEtaria = FaixaEtaria

    def get_Ano_idAno(self):
        return self.Ano_idAno

    def set_Ano_idAno(self, Ano_idAno) :
        self.Ano_idAno = Ano_idAno

