class EstadoObj():
    def __init__(self):
        self.idEstado = None
        self.nome = None
        self.sigla =None
        
    def get_idEstado(self):
        return self.idEstado

    def set_idEstado(self, idEstado) :
        self.idEstado = idEstado

    def get_nome(self):
        return self.nome
    
    def set_nome(self, nome) :
        self.nome = nome

    def get_sigla(self):
        return self.sigla
    
    def set_sigla(self, sigla) :
        self.sigla = sigla

    